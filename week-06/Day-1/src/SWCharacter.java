public class SWCharacter {
   String name;
   Double height;
   Double mass;
   String hair_color;
   String skin_color;
   String eye_color;
   Integer birth_year;
   String gender;

   public SWCharacter(
           String name,
           Double height,
           Double mass,
           String hair_color,
           String skin_color,
           String eye_color,
           Integer birth_year,
           String gender){
       this.name=name;
       this.height=height;
       this.mass=mass;
       this.hair_color=hair_color;
       this.skin_color=skin_color;
       this.eye_color=eye_color;
       this.birth_year=birth_year;
       this.gender=gender;
   }
}
